package main;

public class String_Type {
	private String s;
	private main.DataStructure.Type t;
	
	
	
	public String_Type(String s, main.DataStructure.Type t) {
		this.s = s;
		this.t = t;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public main.DataStructure.Type getT() {
		return t;
	}
	public void setT(main.DataStructure.Type t) {
		this.t = t;
	}

	
}
